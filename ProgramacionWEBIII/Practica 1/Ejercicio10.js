/*Callbacks:
Son útiles cuando solo hay una acción simple y rápida después de otra.
Ejemplo: ejecutar una función después de 1 segundo.
Problema: si hay muchas operaciones anidadas → aparece el Callback Hell (código difícil de leer).
Además, los callbacks dependen de pasar funciones como parámetros, lo que puede complicar la depuración y el manejo de errores.
Promesas:
Se usan cuando hay tareas asíncronas encadenadas (ej: pedir datos a un servidor, procesarlos, luego guardarlos).
Ventaja: .then() y .catch() hacen el código más limpio y fácil de mantener.
Permiten manejar errores de manera centralizada y se integran fácilmente con async/await, lo que hace el código mucho más legible y parecido a la programación síncrona.*/
